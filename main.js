// --- Typing Animation Logic ---
class TypingEffect {
  constructor(element, words, waitTime = 2000) {
    this.element = element;
    this.words = words;
    this.txt = '';
    this.wordIndex = 0;
    this.waitTime = parseInt(waitTime, 10);
    this.isDeleting = false;
    this.type();
  }

  type() {
    const current = this.wordIndex % this.words.length;
    const fullTxt = this.words[current];

    if (this.isDeleting) {
      this.txt = fullTxt.substring(0, this.txt.length - 1);
    } else {
      this.txt = fullTxt.substring(0, this.txt.length + 1);
    }

    this.element.innerHTML = `<span class="typed-text">${this.txt}</span><span class="typed-cursor">|</span>`;

    let typeSpeed = 100;
    if (this.isDeleting) {
      typeSpeed /= 2;
    }

    if (!this.isDeleting && this.txt === fullTxt) {
      typeSpeed = this.waitTime;
      this.isDeleting = true;
    } else if (this.isDeleting && this.txt === '') {
      this.isDeleting = false;
      this.wordIndex++;
      typeSpeed = 400;
    }

    setTimeout(() => this.type(), typeSpeed);
  }
}

// --- Dynamic Projects Loader ---
function loadProjects() {
  const container = document.getElementById("projects-grid");
  if (!container) return;

  container.innerHTML = "";

  projectsData.forEach((project, idx) => {
    // Generate carousel slides
    let carouselIndicators = "";
    let carouselItems = "";
    
    project.images.forEach((img, i) => {
      const activeClass = i === 0 ? "active" : "";
      carouselIndicators += `
        <button type="button" data-bs-target="#carousel-${project.id}" data-bs-slide-to="${i}" class="${activeClass}" aria-label="Slide ${i + 1}"></button>
      `;

      let mediaHtml = "";
      if (img.endsWith(".mp4")) {
        mediaHtml = `
          <video class="d-block w-100" muted loop playsinline preload="metadata">
            <source src="${img}" type="video/mp4">
            Your browser does not support the video tag.
          </video>
        `;
      } else {
        mediaHtml = `<img src="${img}" class="d-block w-100" alt="${project.name} image ${i + 1}" loading="lazy">`;
      }

      carouselItems += `
        <div class="carousel-item ${activeClass}">
          ${mediaHtml}
        </div>
      `;
    });

    // Generate tags
    const tagsHtml = project.software.map(sw => `<span class="software-tag">${sw}</span>`).join("");

    // Setup fade entrance class depending on layout position
    const animationClass = idx % 2 === 0 ? "reveal reveal-left" : "reveal reveal-right";

    const cardHtml = `
      <div class="col-lg-4 col-md-6 mb-4 ${animationClass}">
        <div class="project-card glass-panel">
          <div id="carousel-${project.id}" class="carousel slide project-carousel" data-bs-ride="false" data-bs-interval="false">
            <div class="carousel-indicators">
              ${carouselIndicators}
            </div>
            <div class="carousel-inner">
              ${carouselItems}
            </div>
            <button class="carousel-control-prev" type="button" data-bs-target="#carousel-${project.id}" data-bs-slide="prev">
              <span class="carousel-control-prev-icon" aria-hidden="true"></span>
              <span class="visually-hidden">Previous</span>
            </button>
            <button class="carousel-control-next" type="button" data-bs-target="#carousel-${project.id}" data-bs-slide="next">
              <span class="carousel-control-next-icon" aria-hidden="true"></span>
              <span class="visually-hidden">Next</span>
            </button>
          </div>
          <div class="project-content">
            <h3 class="h5 mb-2">${project.name}</h3>
            <div class="mb-3 d-flex flex-wrap">${tagsHtml}</div>
            <p class="project-desc-short text-secondary">${project.desc}</p>
            <button class="btn btn-glass btn-glass-primary mt-auto w-100 justify-content-center" onclick="showProjectDetails('${project.id}')">
              <i class="bi bi-info-circle"></i> View Specifications
            </button>
          </div>
        </div>
      </div>
    `;

    container.insertAdjacentHTML("beforeend", cardHtml);
  });

  // Start videos playing when hovered inside the carousel
  document.querySelectorAll('.project-carousel').forEach(car => {
    car.addEventListener('mouseenter', () => {
      const activeVideo = car.querySelector('.carousel-item.active video');
      if (activeVideo) activeVideo.play().catch(() => {});
    });
    car.addEventListener('mouseleave', () => {
      const activeVideo = car.querySelector('.carousel-item.active video');
      if (activeVideo) activeVideo.pause();
    });
    
    // Autoplay active video on slide change
    car.addEventListener('slide.bs.carousel', (e) => {
      const oldVideo = e.from !== undefined ? e.target.querySelectorAll('.carousel-item')[e.from].querySelector('video') : null;
      if (oldVideo) oldVideo.pause();
      
      const newVideo = e.to !== undefined ? e.target.querySelectorAll('.carousel-item')[e.to].querySelector('video') : null;
      if (newVideo) {
        // Only play if mouse is hovered over carousel
        if (car.matches(':hover')) {
          newVideo.play().catch(() => {});
        }
      }
    });
  });
}


// --- Gallery Loader & Interactive Filtering ---
function loadGallery() {
  const filtersContainer = document.getElementById("gallery-filters-container");
  const gridContainer = document.getElementById("gallery-grid-container");
  if (!filtersContainer || !gridContainer) return;

  // Clear previous
  filtersContainer.innerHTML = "";
  gridContainer.innerHTML = "";

  // Add "All" button
  filtersContainer.innerHTML = `
    <button class="gallery-filter-btn active" data-filter="all">All Projects</button>
  `;

  // Track added project filter buttons to avoid duplicates
  const addedFilters = new Set();

  projectsData.forEach(project => {
    if (!addedFilters.has(project.folder)) {
      addedFilters.add(project.folder);
      filtersContainer.insertAdjacentHTML(
        "beforeend",
        `<button class="gallery-filter-btn" data-filter="${project.id}">${project.name}</button>`
      );
    }

    // Populate all images into the gallery grid
    project.images.forEach((img) => {
      const itemHtml = `
        <div class="gallery-item" data-project="${project.id}" onclick="openLightbox('${img}')">
          ${img.endsWith('.mp4') ? 
            `<video class="w-100 h-100" style="object-fit: cover;" muted preload="metadata"><source src="${img}" type="video/mp4"></video>` : 
            `<img src="${img}" alt="${project.name} gallery image" loading="lazy">`
          }
          <div class="gallery-overlay">
            <div class="gallery-icon">
              <i class="bi ${img.endsWith('.mp4') ? 'bi-play-fill' : 'bi-zoom-in'} fs-4"></i>
            </div>
            <span class="fw-semibold text-white small">${project.name}</span>
            <span class="text-secondary small mt-1 font-monospace" style="font-size: 0.75rem;">${img.split('/').pop()}</span>
          </div>
        </div>
      `;
      gridContainer.insertAdjacentHTML("beforeend", itemHtml);
    });
  });

  // Filter functionality
  const buttons = document.querySelectorAll(".gallery-filter-btn");
  const items = document.querySelectorAll(".gallery-item");

  buttons.forEach(btn => {
    btn.addEventListener("click", () => {
      // Toggle active classes
      buttons.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");

      const filterVal = btn.getAttribute("data-filter");

      items.forEach(item => {
        if (filterVal === "all" || item.getAttribute("data-project") === filterVal) {
          item.classList.remove("hidden");
        } else {
          item.classList.add("hidden");
        }
      });
    });
  });
}

// --- Lightbox Functionality ---
window.openLightbox = function(src) {
  const lightboxBody = document.getElementById("lightbox-body");
  if (!lightboxBody) return;

  if (src.endsWith(".mp4")) {
    lightboxBody.innerHTML = `
      <video class="img-fluid lightbox-img" controls autoplay>
        <source src="${src}" type="video/mp4">
        Your browser does not support the video tag.
      </video>
    `;
  } else {
    lightboxBody.innerHTML = `<img src="${src}" class="img-fluid lightbox-img" alt="Enlarged view">`;
  }

  const lightboxModalEl = document.getElementById("lightboxModal");
  const modal = bootstrap.Modal.getOrCreateInstance(lightboxModalEl);
  modal.show();

  // Stop video in background when modal is hidden
  lightboxModalEl.addEventListener('hidden.bs.modal', function () {
    lightboxBody.innerHTML = '';
  }, { once: true });
};

// --- Scroll Reveal Trigger ---
function initScrollReveal() {
  const revealElements = document.querySelectorAll(".reveal");
  
  const observer = new IntersectionObserver((entries, observerInstance) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("active");
        
        // If it's a card with skills progress bar, animate the width
        const progressBars = entry.target.querySelectorAll(".progress-bar-fill");
        progressBars.forEach(bar => {
          const val = bar.getAttribute("data-progress");
          bar.style.width = val + "%";
        });

        // Unobserve once shown to keep animations smooth
        observerInstance.unobserve(entry.target);
      }
    });
  }, {
    threshold: 0.12,
    rootMargin: "0px 0px -40px 0px"
  });

  revealElements.forEach(el => observer.observe(el));
}

// --- Contact Form Handler ---
function initContactForm() {
  const form = document.getElementById("portfolio-contact-form");
  if (!form) return;

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    
    // Simple client-side check
    const name = document.getElementById("contact-name").value.trim();
    const email = document.getElementById("contact-email").value.trim();
    const subject = document.getElementById("contact-subject").value.trim();
    const message = document.getElementById("contact-message").value.trim();

    if (!name || !email || !subject || !message) {
      alert("कृपया सभी आवश्यक फील्ड भरें।");
      return;
    }

    // Success styling and popup
    const submitBtn = form.querySelector("button[type='submit']");
    const originalText = submitBtn.innerHTML;
    
    submitBtn.disabled = true;
    submitBtn.innerHTML = `<span class="spinner-border spinner-border-sm me-2" role="status" aria-hidden="true"></span> Sending...`;

    // Simulate sending network request
    setTimeout(() => {
      // Clear inputs
      form.reset();
      submitBtn.disabled = false;
      submitBtn.innerHTML = originalText;

      // Show success modal
      const successModalEl = document.getElementById("successModal");
      if (successModalEl) {
        const successModal = bootstrap.Modal.getOrCreateInstance(successModalEl);
        successModal.show();
      } else {
        alert("आपका संदेश सफलतापूर्वक प्राप्त हुआ! हम जल्द ही आपसे संपर्क करेंगे।");
      }
    }, 1500);
  });
}

// --- App Initialization ---
document.addEventListener("DOMContentLoaded", () => {
  // Navigation active state highlight on scroll
  const sections = document.querySelectorAll("section[id]");
  window.addEventListener("scroll", () => {
    let scrollY = window.pageYOffset;
    
    sections.forEach(current => {
      const sectionHeight = current.offsetHeight;
      const sectionTop = current.offsetTop - 120;
      const sectionId = current.getAttribute("id");
      
      const navLink = document.querySelector(`.nav-link[href*=${sectionId}]`);
      if (navLink) {
        if (scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
          navLink.classList.add("active");
        } else {
          navLink.classList.remove("active");
        }
      }
    });

    // Sticky Navbar class add
    const navbar = document.querySelector(".navbar-glass");
    if (navbar) {
      if (window.scrollY > 50) {
        navbar.classList.add("scrolled");
      } else {
        navbar.classList.remove("scrolled");
      }
    }
  });

  // Typing Title Initiation
  const typingElement = document.getElementById("typing-target");
  if (typingElement) {
    const titles = ["Product Design Engineer","Electromechanical Design Engineer",];
    new TypingEffect(typingElement, titles, 2500);
  }

  // Load Projects and Gallery
  loadProjects();
  loadGallery();
  
  // Initiate Scroll animations
  initScrollReveal();

  // Initiate Form listener
  initContactForm();
});
